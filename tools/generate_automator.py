def generate_script(project_folder_name):
    # Determine paths
    inputs_dir = Path(BASE_PATH) / "ğŸ‡«ğŸ‡· Conscious Movie Factory" / "inputs" / "Coach Adele"
    project_path = inputs_dir / project_folder_name
    
    if not project_path.exists():
        print(f"Error: Project folder not found: {project_path}")
        return

    # Extract Project ID (e.g., "06_50-12 Monia")
    project_id = project_folder_name
    
    # Fill template
    try:
        script_content = TEMPLATE.format(
            project_id=project_id,
            project_path=str(project_path),
            base_path=BASE_PATH,
            wf_diagnose=load_workflow_content("cmf-phase1a-diagnose"),
            wf_narrative=load_workflow_content("cmf-phase1a-narrative"),
            wf_script=load_workflow_content("cmf-phase1a-script"),
            wf_sonic=load_workflow_content("cmf-phase1b-sonic"),
            wf_storyboard=load_workflow_content("cmf-phase1b-storyboard"),
            wf_motion=load_workflow_content("cmf-phase1b-motion"),
            wf_eroll=load_workflow_content("cmf-eroll"),
            wf_authorize=load_workflow_content("cmf-phase1b-authorize"),
        )
        
        # Write file
        output_file = project_path / "RUN_PIPELINE.ps1"
        output_file.write_text(script_content, encoding="utf-8")
        
        print(f"âœ“ Created: {output_file}")
    except Exception as e:
        print(f"Error formatting template for {project_id}: {e}")

